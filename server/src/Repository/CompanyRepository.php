    /* recalculateRating, run sql query to get all reviews for a company, calculate the average rating, and update the company's rating property */
    public function recalculateRating(Company $company): void
    {
        $conn = $this->getEntityManager()->getConnection();
        $sql = '
            SELECT AVG(rating) as rating
            FROM review
            WHERE company_id = :company
        ';
        $stmt = $conn->prepare($sql);
        $res = $stmt->executeQuery(['company' => $company->getId()]);
        $result = $res->fetchOne();
        $company->setReviewAverage($result);
        $this->getEntityManager()->persist($company);
        $this->getEntityManager()->flush();
    }

